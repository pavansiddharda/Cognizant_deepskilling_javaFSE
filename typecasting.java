public class typecasting {
    public static void main(String[] args) {
        int num1=100;
        double y=num1;
        System.out.println(num1);
        System.out.println(y);
        double num2=99.99;
        int i=(int)num2;
        System.out.println(num2);
        System.out.println(i);
    }
}